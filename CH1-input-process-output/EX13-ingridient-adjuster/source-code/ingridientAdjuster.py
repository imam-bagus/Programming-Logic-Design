sugar = 1.5
butter = 1
flour = 2.75

# Input
cookie = int(input("How Many cookies :"))

# Process
totalSugar = sugar * cookie
totalButter = butter * cookie
totalFlour = flour * cookie

# Output
print("Cookie       :", cookie)
print("Need Sugar   :", totalSugar, "Cups")
print("Need Butter  :", totalButter, "Cups")
print("Need Flour   :", totalFlour, "Cups")
