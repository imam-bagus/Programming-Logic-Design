  case 1:
    print("I");
    break;
  case 2:
    print("II");
    break;
  case 3:
    print("III");
    break;
  case 4:
    print("IV");
    break;
  case 5:
    print("V");
    break;
  case 6:
    print("VI");
    break;
  case 7:
    print("VII");
    break;
  case 8:
    print("VIII");
    break;
  case 9:
    print("IX");
    break;
  case 10:
    print("X");
    break;
  default:
    print("Input not valid !!! enter a number range 1 through 10");
    break;
}
